#include "ft_printf.h"

void	check_flags(char *f, t_arg *a)
{
	while (f[a->i] == '-' || f[a->i] == '+' || f[a->i] == ' ' || f[a->i] == '#')
	{
		if (f[a->i] == '-')
			a->f_minest = 1;
		if (f[a->i] == '+')
			a->f_plus = 1;
		if (f[a->i] == ' ')
			a->f_space = 1;
		if (f[a->i] == '#')
			a->f_hash = 1;
		a->i++;
	}
	check_min_lenght(f, a);
}
